export const getUniversityBySlug = (slug: string): UniversityData | null => {
  const slugAliases: Record<string, string> = {
    mit: 'massachusetts-institute-of-technology-mit',
    'university-college-london-ucl': 'university-college-london',
    'university-of-british-columbia-ubc': 'university-of-british-columbia',
    'national-university-of-singapore-nus': 'national-university-of-singapore',
    'nanyang-technological-university-ntu': 'nanyang-technological-university',
    'singapore-university-of-technology': 'singapore-university-of-technology-and-design',
    'victoria-university': 'victoria-university-wellington',
    'university-of-technology-sydney-uts': 'university-of-technology-sydney',
    'london-school-of-economics-and-political-science': 'london-school-of-economics',
  };

  const resolvedSlug = slugAliases[slug] || slug;
  const directMatch = UNIVERSITY_DATABASE[resolvedSlug];

  if (directMatch) {
    return directMatch;
  }

  const fallbackMatch = Object.values(UNIVERSITY_DATABASE).find(
    (university) => generateUniversitySlug(university.name) === slug
  );

  return fallbackMatch || null;
};

